export function getProduct(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getVendor(slug: string): Vendor | undefined {
  return vendors.find((v) => v.slug === slug);
}

export function productsByVendor(vendorSlug: string): Product[] {
  return products.filter((p) => p.vendorSlug === vendorSlug);
}

export function formatPrice(p: Product): string {
  if (p.priceType === "inquiry") return "Inquire";
  const formatted = `$${p.price.toLocaleString()}`;
  if (p.priceType === "from") return `From ${formatted}`;
  if (p.productType === "subscription") {
    return `${formatted}/${p.billingCycle === "weekly" ? "wk" : "mo"}`;
  }
  return formatted;
}

export function productCTA(p: Product): string {
  switch (p.productType) {
    case "call":
      return "Book call";
    case "appointment":
      return "Book";
    case "subscription":
      return "Subscribe";
    case "inquiry":
      return "Inquire";
    case "drop":
      return p.stockLeft && p.stockLeft > 0 ? "Cop" : "Sold out";
    case "bid":
      return "Make offer";
    case "internship":
      return "Apply";
    default:
      return "Add";
  }
}
